// HasAlternateSpelling returns the first known alternate spelling
// for an English word.
//
// This is designed for building search indexes, so the specific
// locale/region of the alternate spelling is not accepted or returned.
func HasAlternateSpelling(word string) string {
	for _, x := range uk_us {
		for i, y := range x {
			if word == y {
				if i == 0 {
					return x[1]
				}
				return x[0]
			}
		}
	}
	return ""
}

